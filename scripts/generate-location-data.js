// Generate CSV content
function generateCSV() {
  let csvContent = "District,Taluk,Type,Name,Classification\n"

  locationData.taluks.forEach((taluk) => {
    // Add cities
    taluk.cities.forEach((city) => {
      csvContent += `"${locationData.district}","${taluk.name}","City","${city.name}","${city.classification}"\n`
    })

    // Add villages
    taluk.villages.forEach((village) => {
      csvContent += `"${locationData.district}","${taluk.name}","Village","${village}","Village"\n`
    })
  })

  return csvContent
}

// Generate summary statistics
function generateSummary() {
  let summary = "DAKSHINA KANNADA DISTRICT - LOCATION SUMMARY\n"
  summary += "=".repeat(50) + "\n\n"

  summary += `District: ${locationData.district}\n`
  summary += `Total Taluks: ${locationData.taluks.length}\n\n`

  let totalCities = 0
  let totalVillages = 0

  locationData.taluks.forEach((taluk) => {
    summary += `${taluk.name}:\n`
    summary += `  Cities: ${taluk.cities.length}\n`
    summary += `  Villages: ${taluk.villages.length}\n`
    summary += `  Total: ${taluk.cities.length + taluk.villages.length}\n\n`

    totalCities += taluk.cities.length
    totalVillages += taluk.villages.length
  })

  summary += `OVERALL TOTALS:\n`
  summary += `Total Cities/Towns: ${totalCities}\n`
  summary += `Total Villages: ${totalVillages}\n`
  summary += `Grand Total: ${totalCities + totalVillages}\n`

  return summary
}

